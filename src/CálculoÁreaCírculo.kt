fun main() {

    // Pegamos o valor do teclado
    println("Digite o raio do círculo:")
    val circleRadius = readln().toDouble()

    // fazemos o cálculo para descobrir a área do raio
    val area = 3.14159 + circleRadius * circleRadius

    // Mostramos ao usuário a área
    println("A área do círculo é: $area ")


}